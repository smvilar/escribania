    <h2 class="mt-2 mb-4 sm:mt-4 sm:mb-8">
      ¿Qué es el derecho de autoprotección?
    </h2>
    <p>
      Se trata de todas las <i
        >disposiciones, instrucciones y decisiones previas</i
      >
      que hacen a nuestro plan de vida, de nuestros deseos, temores, preocupaciones,
      preferencias; la posibilidad jurídicamente válida, de
      <i
        >dejar por escrito lo que deseamos para la posibilidad que en el futuro
        no podamos decidir</i
      >, por ej. por estar en coma o haber sufrido trastornos cognitivos;
      podemos elegir la o las personas que se ocupen de las cuestiones
      personales como decisiones médicas o temas patrimoniales; decidir dónde
      pasar los últimos días, qué hacer con la vivienda; y con la mascota, qué
      tipo de tratamientos e intervenciones considero invasivas y siendo
      inútiles decido rechazarlas; entre muchas otras cuestiones que pueden ser
      de interés personal; lo que se puede conversar con el médico de cabecera y
      finalmente ser incorporado a la historia clínica.
    </p>
    <p>
      Por diversos <i>factores</i> las personas tenemos inquietudes por el futuro
      y buscamos la posibilidad de intervenir en él con nuestras propias decisiones.
      Posiblemente por la modificación de la constitución de la familia, por otro
      lado debido al avance de la medicina que, en ocasiones, en busca de la prolongación
      de la vida, genera condiciones inaceptables para ciertas personas y que la
      conducen a una pérdida de su dignidad. Esta cuestión se ha ido incrementando
      progresivamente en las últimas décadas y sobre todo en la pandemia desencadenó
      un mayor interés aún.
    </p>
    <p>
      La denominación <i>“actos de autoprotección”</i> fue propuesta por la
      delegación española en las VIII Jornadas Notariales Iberoamericanas de
      Veracruz (México) en 1998 y es la más generalizada en Argentina dentro del
      ámbito notarial, Sin embargo, tanto en el Derecho Comparado como en
      nuestro país, ha recibido diferentes denominaciones. Se habla así de
      <i>“testamento para la vida”</i>
      (traducción literal de <i>“living will”</i>, como se lo denomina en el
      derecho anglosajón),
      <i>“disposiciones y estipulaciones para la propia incapacidad”</i>,
      <i>“previsiones para la eventual pérdida del discernimiento”</i>, entre
      otras.
    </p>
    <br />
    <p>
      En Argentina del artículo 19 de nuestra Constitución Nacional surge con
      claridad la esfera de privacidad personal que apenas admite como
      limitaciones el orden público y derechos de terceros.
    </p>
    <p>
      Asimismo, la ley 26529 de 2009 y su modificación por ley 26742 de 2012, en
      el ámbito de la salud, así como los artículos 60 y 139 del Código Civil y
      Comercial desde 2015 regulan actos de autoprotección, como las <i
        >directivas médicas anticipadas y el nombramiento del propio
        apoyo/curador</i
      >.
    </p>
    <p>
      Y ya con anterioridad tanto la Convención sobre los Derechos del Niño<sup
        ><a href="#1">[1]</a></sup
      >, la Convención sobre los Derechos de las Personas con Discapacidad<sup
        ><a href="#2">[2]</a></sup
      >
      (ambas gozan de jerarquía constitucional en nuestro país) y como la reciente
      Convención Interamericana sobre la protección de los Derechos Humanos de las
      Personas Mayores<sup><a href="#3">[3]</a></sup>
      reconocen el respeto de la
      <i>autonomía</i>
      de todas las personas, en cualquier situación en la que se encuentren, en la
      medida en que su madurez, aptitudes mentales o intelectuales lo permitan, para
      <i>decidir</i> anticipadamente acerca del propio destino.
    </p>
    <p>
      La trascendencia de las decisiones plasmadas en un acto de autoprotección,
      requiere que se lo rodee de las mayores <i>garantías</i>. Es fundamental,
      para asegurar su cumplimiento, garantizar la autenticidad de su contenido
      como fiel reflejo de la voluntad del otorgante. La
      <i>escritura pública</i> es el medio que asegura la autenticidad del acto,
      su fecha cierta, su matricidad, el pleno discernimiento, la intención y la
      libertad con que el acto fue otorgado y el debido asesoramiento previo.
    </p>
    <p>
      La <i>inscripción</i> de estos actos en los Registros que funcionan en los
      colegios notariales de las diversas provincias argentinas, resulta de fundamental
      importancia para su conocimiento oportuno.
    </p>
    <p class="font-bold italic">
      Tomar las decisiones hoy para que no sean tomadas por alguien más en el
      futuro. Asesórese con su escribano/a de confianza.
    </p>
